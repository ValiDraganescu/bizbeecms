/**
 * Inline Tailwind v4's CSS source files into a generated TS module so the
 * runtime compiler (`tw-compile.ts`) can resolve `@import "tailwindcss"` from
 * memory — NO filesystem at request time (Cloudflare Workers have none).
 *
 * Tailwind's `compile()` is pure JS (no oxide, no FS) when you hand it an
 * explicit class list; the only thing it needs from disk is the CSS that
 * `@import "tailwindcss"` expands to. We bundle those 4 small files (~57KB)
 * here. Re-run when bumping the tailwindcss dependency:
 *
 *   node scripts/gen-tw-sources.mjs
 */
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const tw = join(root, "node_modules", "tailwindcss");
const files = ["index.css", "theme.css", "preflight.css", "utilities.css"];

const lit = (s) => "`" + s.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${") + "`";

const entries = files
  .map((f) => `  ${JSON.stringify(f)}: ${lit(readFileSync(join(tw, f), "utf8"))},`)
  .join("\n");

const out = `/**
 * GENERATED by scripts/gen-tw-sources.mjs — DO NOT EDIT.
 * Tailwind v${JSON.parse(readFileSync(join(tw, "package.json"), "utf8")).version} CSS sources, inlined for in-Worker compile.
 */
export const TW_SOURCES: Record<string, string> = {
${entries}
};
`;

writeFileSync(join(root, "src", "lib", "render", "tw-sources.generated.ts"), out);
console.log("wrote src/lib/render/tw-sources.generated.ts");
